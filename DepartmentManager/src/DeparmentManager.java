    private void initDepTree() {
        String depRegex = "^(\\w+)-(.+):(.*)$";//G1 code , G2 Name
        String empRegex = "^(\\w+),(.+),(\\d+)(.*)$";//G1 Code, G2 Name, G3 Salary
        DefaultMutableTreeNode currentDepNode = null;
        try {
//            JFileChooser f  = new JFileChooser();
//            f.showOpenDialog(this);
            BufferedReader reader = new BufferedReader(new FileReader(new File(dataPath)));
            while (reader.ready()) {
                String line = reader.readLine();

                //is Department
                if (line.matches(depRegex)) {
                    System.out.println(line);
                    Pattern depPattern = Pattern.compile(depRegex);
                    Matcher depMatcher = depPattern.matcher(line);
                    //WARNING:
                    //De su dung duoc cac ham group thi can phai co ham matches de thong bao cho mathcer
                    if (depMatcher.matches()) {
                        System.out.println(depMatcher.group(1) + " " + depMatcher.group(2));
                        DefaultMutableTreeNode depNode = new DefaultMutableTreeNode(new Department(depMatcher.group(1), depMatcher.group(2)));
                        currentDepNode = depNode;
                        root.add(depNode);
                    }

                } else if (line.matches(empRegex)) {//is Employee
                    Pattern empPattern = Pattern.compile(empRegex);
                    Matcher empMatcher = empPattern.matcher(line);
                    if (empMatcher.matches()) {
                        DefaultMutableTreeNode empNode = new DefaultMutableTreeNode(new Employee(empMatcher.group(1), empMatcher.group(2), Integer.parseInt(empMatcher.group(3))));
                        currentDepNode.add(empNode);
                    }
                }
            }
            reader.close();

        } catch (Exception ex) {
            Logger.getLogger(DeparmentManager.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

