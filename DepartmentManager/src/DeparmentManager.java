    private void updateContentInfo() {

        txtDepCode.setText(curDepNode != null ? ((Department) curDepNode.getUserObject()).getDepCode() : "");
        txtDepName.setText(curDepNode != null ? ((Department) curDepNode.getUserObject()).getDepName() : "");

        txtDepCode.setEditable(addDepMode);
        txtDepName.setEditable(addDepMode);
        
        
        txtEmpCode.setText(curEmpNode != null ? ((Employee) curEmpNode.getUserObject()).getEmpCode() : "");
        txtEmpName.setText(curEmpNode != null ? ((Employee) curEmpNode.getUserObject()).getEmpName() : "");
        txtEmpSalary.setText(curEmpNode != null ? Integer.toString(((Employee) curEmpNode.getUserObject()).getSalary()) : "");

        txtEmpCode.setEditable(addEmpMode);
        txtEmpName.setEditable(addEmpMode);
        txtEmpSalary.setEditable(addEmpMode);
        
    }

    private void depTreeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_depTreeMouseClicked
        // TODO add your handling code here:

        TreePath selectionPath = this.depTree.getSelectionPath();
        DefaultMutableTreeNode selectedNode = (DefaultMutableTreeNode) selectionPath.getLastPathComponent();
        if (selectionPath == null || selectedNode == root) {
            return;
        }
        //turn off save btn
        addDepMode = false;
        addEmpMode = false;
        txtDepCode.setEditable(true);
            txtDepName.setEditable(true);
            txtEmpCode.setEditable(true);
            txtEmpName.setEditable(true);
            txtEmpSalary.setEditable(true);
        //TH selected node la depNode
            
        if (selectedNode.getUserObject() instanceof Department) {

            curDepNode = selectedNode;
            curEmpNode = null;
            updateContentInfo();

        } else {
            curEmpNode = selectedNode;
            updateContentInfo();
        }

    }//GEN-LAST:event_depTreeMouseClicked

