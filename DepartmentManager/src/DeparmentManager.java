    private boolean isValidEmpInput(){
        
        String salary = txtEmpSalary.getText();
        return txtEmpCode.getText().trim().length() > 0 && txtEmpName.getText().trim().length() > 0 && salary.matches("^\\d+$");
    }
    
    
    private void saveDepBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveDepBtnMouseClicked
        // TODO add your handling code here:
        if(!isValidDepInput())
            JOptionPane.showMessageDialog(this, "Not valid input");
        else{
            if(addDepMode){//save new
                DefaultMutableTreeNode newDepNode = new DefaultMutableTreeNode(new Department(txtDepCode.getText(), txtDepName.getText()));
                root.add(newDepNode);
            }else{
                Department curDepartment = (Department) curDepNode.getUserObject();
                curDepartment.setDepCode(txtDepCode.getText());
                curDepartment.setDepName(txtDepName.getText());
            }
            this.depTree.updateUI();
            addDepMode = false;
            curDepNode = null;
        }
    }//GEN-LAST:event_saveDepBtnMouseClicked

    private void saveEmpBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveEmpBtnMouseClicked
        // TODO add your handling code here:
        
        if(curDepNode == null){
            JOptionPane.showMessageDialog(this, "Please choose deapartment");
            return;
        }
        if(!isValidEmpInput())
            JOptionPane.showMessageDialog(this, "Not valid input");
        else{
            if(addEmpMode){
                DefaultMutableTreeNode newEmpNode = new DefaultMutableTreeNode(new Employee(txtEmpCode.getText(), txtEmpName.getText(),Integer.parseInt(txtEmpSalary.getText())));
                curDepNode.add(newEmpNode);
            }else{
                Employee curEmployee = (Employee) curEmpNode.getUserObject();
                curEmployee.setEmpCode(txtEmpCode.getText());
                curEmployee.setEmpName(txtEmpName.getText());
                curEmployee.setSalary(Integer.parseInt(txtEmpSalary.getText()));
            }
            
            this.depTree.updateUI();
            addEmpMode = false;
        }
        
        
    }//GEN-LAST:event_saveEmpBtnMouseClicked

    private void removeDepBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeDepBtnMouseClicked
        // TODO add your handling code here:
        if(curDepNode == null)
            return;
        if(curDepNode.getChildCount() > 0)
            JOptionPane.showMessageDialog(this, "Remove all employee before deleting a department");
        else{
            
            int response = JOptionPane.showConfirmDialog(this, "Do you want to delete this department");
            if(response == JOptionPane.OK_OPTION){
                this.root.remove(curDepNode);
                this.depTree.updateUI();
                updateContentInfo();
                curDepNode= curEmpNode = null ;
            }
        }
        
    }//GEN-LAST:event_removeDepBtnMouseClicked

    private void removeEmpBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeEmpBtnMouseClicked
        // TODO add your handling code here:
        if(curEmpNode == null)
            return;
        int response = JOptionPane.showConfirmDialog(this, "Do you want to delete this Employee");
            if(response == JOptionPane.OK_OPTION){
                curDepNode.remove(curEmpNode);
                curEmpNode = null;
                depTree.updateUI();
                updateContentInfo();
            }
        
    }//GEN-LAST:event_removeEmpBtnMouseClicked

